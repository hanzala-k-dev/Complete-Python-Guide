"""Shows Python data type literals with examples.
Includes numbers, booleans, strings, complex values, and collections.
Each print demonstrates actual output for learner clarity.
"""

# Integer values
print(4)  # Output: 4
print(5000000000000000000000000)  # Output: 5000000000000000000000000
print(1e308)  # Output: 1e+308
print(1e309)  # Output: inf

# Floating point values
print(4.5)  # Output: 4.5
print(1.8e307)  # Output: 1.8e+307
print(1.8e308)  # Output: inf
print(1.9e308)  # Output: inf

# Boolean values
print(True)  # Output: True
print(False)  # Output: False

# Complex number value
print(4 + 5j)  # Output: (4+5j)

# String values using single, double, and triple quotes
print("karachi")  # Output: karachi
print("karachi")  # Output: karachi
print("""karachi""")  # Output: karachi
print("""karachi""")  # Output: karachi

# List value
print([1, 2, 4, 5, 6, 7, 8])  # Output: [1, 2, 4, 5, 6, 7, 8]

# Tuple value
print((1, 2, 3, 4, 5))  # Output: (1, 2, 3, 4, 5)

# Set value
print({1, 2, 3, 4, 5})  # Output: {1, 2, 3, 4, 5}

# Dictionary value with string keys and mixed-type values
print(
    {"Name": "Hanzala", "Age": 20, "City": "karachi"}
)  # Output: {'Name': 'Hanzala', 'Age': 20, 'City': 'karachi'}
